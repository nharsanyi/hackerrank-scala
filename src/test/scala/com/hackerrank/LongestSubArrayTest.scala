package com.hackerrank

import org.scalatest.FunSuite

class LongestSubArrayTest extends FunSuite {

  test("testLongestSubarray 1") {
    assertResult(4)(LongestSubArray.longestSubarray(Array(0, 1, 2, 1, 2, 3)))
    //1,2,1,2
  }

  test("testLongestSubarray 2") {
    assertResult(4)(LongestSubArray.longestSubarray(Array(1, 1, 1, 3, 3, 2, 2)))
    // 3,3,2,2
  }

  test("testLongestSubarray 3") {
    assertResult(2)(LongestSubArray.longestSubarray(Array(1, 2, 3, 4, 5)))
    // 1,2
  }

  test("testLongestSubarray 4") {
    assertResult(3)(LongestSubArray.longestSubarray(Array(3, 2, 2, 1)))
    //2,2,1
  }

}
